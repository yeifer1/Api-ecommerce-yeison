const mongoose = require('mongoose');

const productSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  category: {
    type: String,
    required: true,
  },
  // Otros campos relacionados con el producto, como im√°genes, existencias, etc.
});

const Product = mongoose.model('Product', productSchema);

module.exports = Product;